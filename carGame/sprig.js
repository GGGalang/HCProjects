function spawnCar(){
  addSprite(62, Math.floor(Math.random()*9), "c");
}

var frame = setInterval(function(){
  let carChance = Math.floor(Math.random()*10);
  if (carChance <= (carProb-(1*(diff/1000)))){
    addText("spawned", {
      x: 10,
      y: 4,
      color: color`3`
    })
    spawnCar();
  } else {
    addText("nah",{
      x: 10,
      y: 4,
      color: color`3`
    })
  }

  getAll("c").forEach((cars) => {
    cars.x -= 1;
    if(cars.x == 0){
      clearTile(cars.x, cars.y)
    }
  })

  if (diff>200){
    diff -= 10
  }

}, diff);
